package HomeWork;

class PrimitiveDataType {
	public static void main(String[] args) {
		
		byte a=10;
		short b=20;
		int c=30;
		long d=40; 
		long e=2146483648L;

		System.out.println("byte : "+ a+", short : "+b+", int : " +c+", long : "+d+", "+e);

		char f=65; 
		char g='0';
		char h='°¡';
		System.out.println("char : "+f+","+g+","+h);

		float i=3.14F;
		double j=3.14;

		System.out.println("float : "+i+", double : "+j);

		boolean l=true;
		boolean m=false;
		System.out.println("boolean : "+l+","+m);

	}//main
}//class



















